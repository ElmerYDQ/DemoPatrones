package entidad;

/**
 *
 * @author Diaz
 */
public class Conductor {
    private int idConductor;
    private String nombre;
    private String apellido;
    private String dni;
    private String brevete;
    private String telefono;
    private String direccion;
    private String email;

    public int getIdConductor() {
        return idConductor;
    }

    public void setIdConductor(int idConductor) {
        this.idConductor = idConductor;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public String getBrevete() {
        return brevete;
    }

    public void setBrevete(String brevete) {
        this.brevete = brevete;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public String toString() {
        return "Conductor{" + "idConductor=" + idConductor + ", nombre=" + nombre + ", apellido=" + apellido + ", dni=" + dni + ", brevete=" + brevete + ", telefono=" + telefono + ", direccion=" + direccion + ", email=" + email + '}';
    }
    
}
